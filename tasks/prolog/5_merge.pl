% определить предикат mrg(L1, L2, R) который для двух отсортированных списков L1 и L2
% определяет список R, составленный из этих элементов

mrg([X|L1], [Y|L2], [X|R]) :-
  X < Y, mrg(L1, [Y|L2], R).

mrg([X|L1], [Y|L2], [Y|R]) :-
  Y =< X, mrg([X|L1], L2, R).


mrg([], L2, L2).
mrg(L1, [], L1).
mrg([], [], []).

% Пример

% ?- mrg([1,2,5,6], [3,4,7,8], Y).
% Y = [1, 2, 3, 4, 5, 6, 7, 8] ;
%
%
% ?- mrg([1], [1, 2], Y).
% Y = [1, 1, 2] ;
